class Characters {
    private String name;
    private int level = 1;

    public void setName (String name) {
        this.name = name;
    }

    public String getName () {
        return this.name;
    }

    public void setLevel (int level) {
        this.level = level;
    }

    public int getLevel () {
        return this.level;
    }
}
